
public class LoopsPractice {

	public static void main(String[] args) {
		
		for(int i = 0; i < 5; i++) {
			System.out.println("Hello from for loop 5 times");		
		}
		
		int j = 0; 
		
		while(j < 3) {
			System.out.println("Hello from while loop 3 times");
			j++;
		}
		
		int k = 0; 
		
		do {
			System.out.println("Hello from do-while, this staement will run at least once");
			k++;
		}while(k < 2);

	}

}
